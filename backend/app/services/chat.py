"""
Conversational AI Chat & RAG Service with Citations, Confidence Scoring,
RAG Evaluation Metrics, LangSmith Tracing, and Advanced Retrieval.

Pipeline stages:
  1. Step-Back Prompting   — generates abstract query for broader recall
  2. Query Expansion       — 3 LLM query variants for coverage
  3. Hybrid Search + RRF   — Qdrant semantic + Postgres FTS fused
  4. Parent-Document Expand — child chunks replaced by richer parents
  5. LLM Reranking         — Gemini-scored relevance sorting
  6. Contextual Compression — strips noise from low-score chunks
  7. Agentic Loop (ReAct)  — LLM tool-calls for follow-up retrieval
  8. Final Generation      — structured JSON with citations
  9. RAGAS Eval (async)    — fire-and-forget faithfulness/relevancy scoring
  10. LangSmith Tracing    — full pipeline traced with child spans
"""

